create table SOURCES(
ID INT NOT NULL,
NAME VARCHAR(255) NOT NULL,
FRIENDLY_NAME VARCHAR(255),
PROTOCOL_NAME VARCHAR(255) NOT NULL,
ACTIVE INT DEFAULT NULL ,
REASON  VARCHAR(255),
SCHEDULE  VARCHAR(255),
_PRIORITY INT,
RUNNABLE_WINDOW_IN_MINUTES INT,
HOST  VARCHAR(255),
USERNAME  VARCHAR(255),
ENCRYPTED_PASSWORD  VARCHAR(255),
ENCRYPTED INT DEFAULT NULL,
PATH  VARCHAR(255),
PATTERN  VARCHAR(255),
ARCHIVE_FILES INT DEFAULT NULL,
ARCHIVE_FILES_PATH  VARCHAR(255),
TASKS  VARCHAR(255),
ACTION_MODE  VARCHAR(255),
DESTINATION_SOURCE_ID INT,
TIMEZONE  VARCHAR(255),
EXTRA_INFO  VARCHAR(255),
MAIL_LIST  VARCHAR(255),
ALLOWABLE_PAGER_TIMES  VARCHAR(255),
CREATED_AT DATE NOT NULL,
UPDATED_AT DATE NOT NULL,
PRIMARY KEY(ID)
);
CREATE TABLE JOBS(
ID INT NOT NULL,
NAME  VARCHAR(255) NOT NULL,
SOURCE_ID INT,
COMMAND  VARCHAR(255),
STATUS  VARCHAR(255),
PRIORITY INT,
RUNNABLE_AT DATE,
NOT_RUNNABLE_AT DATE,
STARTED_AT DATE,
ENDED_AT DATE,
PID  VARCHAR(255),
LOG_PATH  VARCHAR(255),
MAIL_LIST  VARCHAR(255),
ALLOWABLE_PAGER_TIMES  VARCHAR(255),
CREATED_AT DATE NOT NULL,
UPDATED_AT DATE NOT NULL,
PRIMARY KEY(ID),
FOREIGN KEY(SOURCE_ID) REFERENCES SOURCES(ID) ,
);
CREATE TABLE CHUNKS (
ID INT NOT NULL,
JOB_ID INT,
TMP_PATH  VARCHAR(255),
FINAL_PATH  VARCHAR(255),
CREATED_AT DATE NOT NULL,
UPDATED_AT DATE NOT NULL,
PRIMARY KEY (ID),
FOREIGN KEY(JOB_ID) REFERENCES JOBS(ID)
);
CREATE TABLE DEPENDENCY_JOBS(
ID INT NOT NULL,
JOB_ID INT NOT NULL,
DEPENDENCY_JOB_ID INT NOT NULL,
PRIMARY KEY(ID),
FOREIGN KEY(JOB_ID) REFERENCES CHUNKS(ID)
);
